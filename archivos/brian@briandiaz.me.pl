random_elementos(_, _, 0, []):-!.
random_elementos(L, N, C, Res) :- Ce is C-1, random_elementos(L,N,Ce,R), X is random(N), nth0(X,L,E), Cant is random(3), (Cant =:= 0 -> append([[E,1]],R,Res) ; append([[E,Cant]],R,Res)).

